#include "libft.h"

int	ft_atoi(const char *nptr)
{
	char	*str;
	int		result;
	int		sign;

	sign = 1;
	result = 0;
	str = (char *)nptr;
	while (*str == 32 || (*str > 8 && *str < 14))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (result * sign);
}
