#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	*str;

	i = 0;
	str = ft_itoa(n);
	while (str[i])
	{
		write(fd, &str[i], 1);
		i++;
	}
	free(str);
}
