#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	size;
	size_t	i;

	if (!s)
		return (NULL);
	size = 0;
	if (start > ft_strlen(s))
		size = 0;
	else if (len <= (ft_strlen(s) - start))
		size = len;
	else
		size = (ft_strlen(s) - start);
	str = malloc(sizeof(char) * (size + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i < size)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = 0;
	return (str);
}
